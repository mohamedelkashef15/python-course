#################
# Comparsion Operators
# Equal ==
# Not Equal !=
# Greater than >
# less than <
# Greater than or equal >=
# less than or equal <=
#################

# Equal ==
print(5 == 5)
print(6 == 5)

# Not Equal !=
print(5 != 5)
print(10 != 3)

# Greater than >
print(3 > 2)
print(2 > 3)

# less than <
print(5 < 2)
print(2 < 5)

# Greater than or equal >=
print(8 >= 8)
print(8 >= 10)

# less than or equal <=
print(6 <= 6)
print(6 <= 10)
print(6 <= 2)
