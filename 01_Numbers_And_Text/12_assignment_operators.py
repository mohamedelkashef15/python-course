######################
# Assignment Operators
#  =
#  +=
#  -=
#  *=
#  **=
#  /=
#  %=
######################
name = "Mohamed"
print(name)

# new Name = old name + " Elkashef"
name = name + " Elkashef"
print(name)

x = 3
print(x)

# new x = old x + 2
x = x + 2 # 5
x += 2
print(x) # 7

y = 10
# y = y - 4
y -= 4

print(y)

z = 5
z *= 3
# z = z * 3
print(z)

a = 5
# a = a ** 3
# 5 * 5 * 5
a **= 3

print(a)