#####################
# Booleans
#   Compare between values
#   False Values
#   True Values
#####################

# Compare between values
print(10 > 7)
print(7 > 10)

# False Values
print(bool(False))
print(bool(0))
print(bool(None))
print(bool(""))
print(bool([]))
print(bool(()))
print(bool({}))

# True Values
print(bool(True))
print(bool(-121))
print(bool("Hello"))
print(bool(["apple", "banana"]))
print(bool(("apple", "banana")))
print(bool({"apple", "banana"}))
