#####################
# Booleans
#   Compare between values
#   False Values
#   True Values
#####################
# Compare between values

print(10 > 9)
print(10 < 9)

# False Values
# bool converts into boolean value
print(bool(False))
print(bool(None))
print(bool(0))
print(bool(""))
print(bool(()))
print(bool([]))
print(bool({}))

# True Values
print(bool(True))
print(bool("Hello"))
print(bool(-5.5))
print(bool(["apple", "banana"]))
print(bool({"apple", "banana"}))
print(bool(("apple", "banana")))

