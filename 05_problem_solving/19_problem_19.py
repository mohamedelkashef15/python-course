# 1- Use built-in functions max and min to get maximum, minimum number
# 2- display result

# * Problem solution
numbers = [8, 3, 15, 20, 6]

maximum = max(numbers)
minimum = min(numbers)

print(f"Maximum Number {maximum}")
print(f"Minimum Number {minimum}")











