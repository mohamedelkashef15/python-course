"""
    1- What is variable ?
    2- Changing variable values
    3- Naming Rules
    4- Assign Multiple Values
"""
# 1 - What is variable ?
name = "Mohamed"
age = 25

print(name)
print(age)

# 2- Changing variable values
score = 10
print(score)  # 10

score = 20
print(score)  # 20

# 3- Naming rules
name = "Alice"
age = 25
total_price = 99.99
is_active = True

# Invalid names:
# 2nd_place = "Bob"   # Cannot start with a digit
# my-variable = 10    # Hyphens are not allowed
# def = "keyword"     # Cannot use reserved keywords


# 4- Assign multiple values at once
a, b, c = 1, 2, 3
print(a, b, c)


