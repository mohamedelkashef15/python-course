#######################
# List Methods
#   - sort()
#   - reverse()
#   - count()
#   - index()
#######################

numbers = [3, 1, 4, 1, 5, 9, 2]

# sort()
numbers.sort()
print(numbers)
# [1, 1, 2, 3, 4, 5, 9]

# reverse()
numbers.reverse()
print(numbers)

# count()
print(numbers.count(1)) # 2

# index()
print(numbers.index(4))  # 1


