######################
# Data Types
#     - integer
#     - float
#     - string
#     - boolean
######################

# integer
age = 25
count = 0
temp = -5

print(age, type(age))
print(count, type(count))
print(temp, type(temp))

# float
pi = 3.14
weight = 66.5

print(pi, type(pi))
print(weight, type(weight))

# string
name = "Mohamed"

print(name, type(name))

# boolean
has_driving_licence = True
is_raining = False

print(has_driving_licence, type(has_driving_licence))
print(is_raining, type(is_raining))


